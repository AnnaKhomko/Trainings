

namespace Calculator
{
    public class Calculation
    {
        public double Add (long a, long b)
        {
            return a + b;
        }

        public double Subtract(long a, long b)
        {
            return a - b;
        }

        public double Multiplicate(long a, long b)
        {
            return a * b;
        }

        public double Divide(long a, long b)
        {
            return a / b;
        }
    }
}
